defc    DISPLAY=0xC000

_screen_fill:
    ld      (fill_pattern), hl
    ld      hl, DISPLAY
    ld      c, 0x80
    ld      de, 16
loop1:
    ld      b, 48
    ld      a, (fill_pattern)
loop2:
    ld      (hl), a
    inc     hl
    djnz    loop2
    add     hl, de
    ld      b, 48
    ld      a, (fill_pattern+1)
loop3:
    ld      (hl), a
    inc     hl
    djnz    loop3
    add     hl, de
    dec     c
    jr      nz, loop1
    ret

_screen_set_region:
    pop     hl      ; return address
    pop     de      ; h
    pop     bc      ; w
    ld      d, c    ; d = width, e = height
    pop     bc      ; y
    ex      (sp), hl ; x
    ld      b, c    ; b = y
    ld      c, l    ; c = x
; de = width, height
; bc = y, x
screen_set_region_asm:
    call    generic_console_xypos
    ld      (video_ptr), hl
    ex      de, hl
    ld      (region_wh), hl
    ret

_screen_fill_region:
    ; save fill pattern
    ld      (fill_pattern), hl
    ex      de, hl
    ; save fill mask
    ld      (fill_mask), hl
    ; preapre variables for loop
    ld      hl, (region_wh)
    ld      a, l
    rlc
    rlc
    ld      c, a    ; c = number of lines to fill / 2 = text rows * (8 / 2)
    ld      a, 64
    sub     a, h    ; 64 - text columns
    ld      e, a    ; e = offset to next line
    ld      hl, (video_ptr)
floop1:
    ; even line
    ld      a, (region_wh+1)
    ld      b, a    ; b = number of bytes to fill
    ld      a, (fill_pattern) ; even line pattern
    ld      d, a    ; d = pattern
floop2:
    ld      a, (fill_mask)
    and     a, (hl) ; apply mask
    xor     a, d    ; apply pattern
    ld      (hl), a
    inc     hl
    djnz    floop2
    ld      d, 0
    add     hl, de  ; next line

    ; odd line
    ld      a, (region_wh+1)
    ld      b, a    ; b = number of bytes to fill
    ld      a, (fill_pattern+1) ; odd line pattern
    ld      d, a    ; d = pattern
floop3:
    ld      a, (fill_mask)
    and     a, (hl) ; apply mask
    xor     a, d    ; apply pattern
    ld      (hl), a ; fill
    inc     hl
    djnz    floop3
    ld      d, 0
    add     hl, de  ; next line
    dec     c       ; decrement line pair counter
    jr      nz, floop1
    ret

defc    BUFFER_ROWS=220

_buffer_to_screen:
    ld      de, _network_buffer + 2 ; input buffer, skip first 2 bytes
    ld      a, BUFFER_ROWS   ; # rows to convert
line_loop:
    ld      (line_count), a
    call    convert_row
    ; skip next 4 bytes on input
    inc     de
    inc     de
    inc     de
    inc     de
    ; skip next 16 bytes on output
    ld      bc, 16
    add     hl, bc
    ; next row?
    ld      a, (line_count)
    dec     a
    jp      nz, line_loop
    ret

; Convert 36 bytes from input to 48 bytes on output
convert_row:
; repeat 12x 3 to 4 conversion
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    call    three_to_four
    ; continue to last three_to_four

; Convert 3 bytes from input to 4 bytes on output
; DE = input pointer
; HL = output pointer
three_to_four:
    ld      b, _swap_table_00 / 256 ; first table
    ld      a, (de) ; input byte 0
    ld      c, a    ; offset in table
    ld      a, (bc) ; value from table
    ld      (hl), a ; output byte 0
    inc     b       ; next table
    ld      a, (bc) ; value from table
    inc     hl      ; next output
    ld      (hl), a ; output byte 1

    inc     de      ; next input
    ld      a, (de) ; input byte 1
    ld      c, a    ; offset in table
    inc     b       ; next table
    ld      a, (bc) ; value from table
    or      (hl)    ; combined
    ld      (hl), a ; output byte 1
    inc     b       ; next table
    ld      a, (bc) ; value from table
    inc     hl      ; next output
    ld      (hl), a ; output byte 2

    inc     de      ; next input
    ld      a, (de) ; input byte 2
    ld      c, a    ; offset in table
    inc     b       ; next table
    ld      a, (bc) ; value from table
    or      (hl)    ; combined
    ld      (hl), a ; output byte 2
    inc     b       ; next table
    ld      a, (bc) ; value from table
    inc     hl      ; next output
    ld      (hl), a ; output byte 3

    inc     de      ; next input
    inc     hl      ; next output

    ret


